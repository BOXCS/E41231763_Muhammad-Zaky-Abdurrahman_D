package Week2.Acara5_6.Tugas2;

public class Person {

    public String getName() {
        return Name;
    }

    public int getAge() {
        return Age;
    }

    public void setAge(int Age) {
        this.Age = Age;
    }

    public Person(String Name, int Age) {
        this.Name = Name;
        this.Age = Age;
    }

    private String Name;
    private int Age;
}
